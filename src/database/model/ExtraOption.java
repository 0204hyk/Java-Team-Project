package database.model;

public class ExtraOption {

	Integer option_number;
	String option_name;
	Integer option_price;
	
	public ExtraOption() {
	}

	public Integer getOption_number() {
		return option_number;
	}

	public void setOption_number(Integer option_number) {
		this.option_number = option_number;
	}

	public String getOption_name() {
		return option_name;
	}

	public void setOption_name(String option_name) {
		this.option_name = option_name;
	}

	public Integer getOption_price() {
		return option_price;
	}

	public void setOption_price(Integer option_price) {
		this.option_price = option_price;
	}
	
	
}
